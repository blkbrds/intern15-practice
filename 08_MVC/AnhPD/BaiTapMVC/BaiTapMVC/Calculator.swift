import Foundation

class Calculator {
    
    func add(a: Float, b: Float) -> Float {
        return a + b
    }
    
    func sub(a: Float, b: Float) -> Float {
        return a - b
    }
    
    func mul(a: Double, b: Double) -> Double {
        return a * b
    }
    
    func  div(a: Double, b: Double) -> Double {
        return a / b
    }
}
